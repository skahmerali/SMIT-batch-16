const mongoose = require("mongoose");

const { Schema } = mongoose;

const user = new Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    required: true,
  },
});

const userModel = mongoose.model("myusers", user);

module.exports = userModel;
